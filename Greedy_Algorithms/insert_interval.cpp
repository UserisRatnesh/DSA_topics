vector<vector<int>> insertNewInterval(vector<vector<int>> &intervals,
                                      vector<int> &newInterval) {

  vector<vector<int>> vec(intervals.begin(), intervals.end());
  vec.push_back(newInterval);

  // sort on the basis of start time
  sort(vec.begin(), vec.end(),
       [](const vector<int> &v1, const vector<int> &v2) {
         return v1[0] < v2[0];
       });

  int n = vec.size();

  vector<vector<int>> ans;

  int startTime = vec[0][0];
  int endTime = vec[0][1];

  for (int i = 1; i < n; i++) {

    int newStartTime = vec[i][0];
    int newEndTime = vec[i][1];

    if (newStartTime <= endTime) {
      // continue this interval
      endTime = max(endTime, newEndTime);
    } else {
      ans.push_back({startTime, endTime});
      startTime = newStartTime;
      endTime = newEndTime;
    }
  }

  ans.push_back({startTime, endTime});
  return ans;
}

