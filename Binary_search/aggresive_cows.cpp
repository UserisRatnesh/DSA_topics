bool isPossible(vector<int> &nums, int mid, int k) {
  int cnt = 1;
  int prev = nums[0];
  int n = nums.size();
  for (int i = 1; i < n; i++) {
    if ((nums[i] - prev) >= mid) {
      cnt++;
      prev = nums[i];
    }
  }

  return cnt >= k;
}

int aggresiveCows(vector<int> &nums, int k) {

  int n = nums.size();

  sort(nums.begin(), nums.end());

  int low = 1;
  int high = nums[n - 1] - nums[0];

  int ans = 0;

  while (low <= high) {
    int mid = low + (high - low) / 2;
    if (isPossible(nums, mid, k)) {
      ans = mid;
      low = mid + 1;
    } else {
      high = mid - 1;
    }
  }

  return ans;
}

