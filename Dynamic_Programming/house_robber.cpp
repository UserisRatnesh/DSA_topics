// code it depends on the code of max_non_adjacent_sum() fun
int house_robber(vector<int> &money) {

  int n = money.size();
  if (n == 1) {
    return money[0];
  }

  vector<int> first, second;
  for (int i = 0; i < n; i++) {
    if (i != 0) {
      first.push_back(money[i]);
    }
    if (i != n - 1) {
      second.push_back(money[i]);
    }
  }

  return max(max_non_adjacent_sum(first), max_non_adjacent_sum(second));
}

