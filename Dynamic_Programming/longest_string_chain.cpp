bool isPossible(string &s1, string &s2) {

  int n1 = s1.size();
  int n2 = s2.size();

  if (n1 != n2 + 1) {
    return false;
  }

  int i = 0, j = 0;
  int diff = 0;

  // HACK: Ye issliye kaam kar rha kyoki s2 ka size s1 se sirf ek hi jyada hoga,
  // Ye humne pahle hi chekc kar liya hai.
  while (i < n1) {
    if (s1[i] == s2[j]) {
      i++;
      j++;
    } else {
      i++;
    }
  }

  return i == n1 && j == n2;
}

bool static compare(string &s1, string &s2) { return s1.size() < s2.size(); }

int longestStringChain(vector<string> &words) {

  sort(words.begin(), words.end(), compare);
  int n = words.size();
  vector<int> dp(n + 1, 1);
  int maxi = 1;

  for (int i = 0; i < n; i++) {
    for (int j = 0; j < i; j++) {

      // Saath me jo jo characters words[j] me hai whi characters words[j] me
      // hone chahiye
      if (isPossible(words[i], words[j]) && dp[i] < dp[j] + 1) {
        dp[i] = dp[j] + 1;
      }
    }
    maxi = max(maxi, dp[i]);
  }

  return maxi;
}

