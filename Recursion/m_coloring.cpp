bool isSafe(int node, int color, vector<int> &colors,
            vector<vector<int>> &adj) {
  for (auto child : adj[node]) {
    if (colors[child] == color) {
      return false;
    }
  }

  return true;
}

bool helper(int node, int m, vector<int> &colors, vector<vector<int>> &adj) {

  int n = colors.size();
  if (node == n) {
    return true;
  }

  for (int c = 0; c < m; c++) {
    if (isSafe(node, c, colors, adj)) {
      colors[node] = c;
      bool ans = helper(node + 1, m, colors, adj);
      colors[node] = -1;

      if (ans) {
        return true;
      }
    }
  }

  return false;
}

bool graphColoring(vector<vector<int>> &edges, int m, int n) {

  vector<vector<int>> adj(n, vector<int>());
  for (auto it : edges) {
    int u = it[0];
    int v = it[1];
    adj[u].push_back(v);
    adj[v].push_back(u);
  }

  vector<int> colors(n, -1);

  bool ans = helper(0, m, colors, adj);

  return ans;
}

