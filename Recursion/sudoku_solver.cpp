bool isSafe(int row, int col, char digit, vector<vector<char>> &board) {

  for (int i = 0; i < 9; i++) {
    if (board[row][i] == digit || board[i][col] == digit) {
      // Digit already in row or column
      return false;
    }
  }
  int startRow = (row / 3) * 3;
  int startCol = (col / 3) * 3;
  for (int i = startRow; i < startRow + 3; i++) {
    for (int j = startCol; j < startCol + 3; j++) {
      if (board[i][j] == digit) {
        // Digit already in 3x3 sub-box
        return false;
      }
    }
  }
  return true;
}

bool helper(vector<vector<char>> &board) {

  for (int i = 0; i < 9; i++) {
    for (int j = 0; j < 9; j++) {
      if (board[i][j] != '.') {
        continue;
      }

      for (char c = '1'; c <= '9'; c++) {
        if (isSafe(i, j, c, board)) {

          board[i][j] = c;
          bool ans = helper(board);

          if (ans) {
            return true;
          }
          board[i][j] = '.';
        }
      }

      // Agar is {i,j}='.' ke liye koi digit possible nahi hai to return false
      return false;
    }
  }

  return true;
}

void solve_sudoku(vector<vector<char>> &board) { helper(board); }

