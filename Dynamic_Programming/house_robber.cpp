// NOTE: max sum of non adjacent elements in array
int max_non_adjacent_sum(vector<int> &nums) {

  int n = nums.size();
  int prev_pick = 0;
  int prev_not_pick = 0;

  for (int i = 0; i < n; i++) {

    int pick = prev_not_pick + nums[i];
    int not_pick = max(prev_pick, prev_not_pick);

    prev_pick = pick;
    prev_not_pick = not_pick;
  }

  return max(prev_not_pick, prev_pick);
}

